package Modelo;

/**
 *
 * @author GM
 */
public class Activo {
    
    private int id;
    private String codigo;

    public Activo(String codigo) {
        this.codigo = codigo;
    }

    public Activo() {
    }
    

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }
    
    
    
}
