    private Usuario comprobarUser(String user, String clave) {
        clave = Encriptacion.getStringMessageDigest(clave, Encriptacion.SHA256);
        ControladorUsuario ctr=new ControladorUsuario();
        Usuario login=new Usuario();
        login.setUser(user);
        login.setPass(clave);
        Usuario x = ctr.loginUsuario(login);
        if (x != null) {
                return x;
        }
        return null;
    }

