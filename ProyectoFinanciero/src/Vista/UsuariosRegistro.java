    public void leerDatos(MouseEvent e) {
        int row = tblDatos.rowAtPoint(e.getPoint());
        posicion = Integer.parseInt(tblDatos.getValueAt(row, 0).toString());
        objeto = new Usuario();
        objeto = usuarios.get(posicion - 1);
        limpiar();
        txtUsuario.setEditable(false);
        lblNueva.setVisible(true);
        checkCambio.setVisible(true);
        checkCambio.setSelected(false);
        jbtmodificar.setEnabled(true);
        txtNuvaContraseña.setVisible(true);
        txtUsuario.setText(objeto.getUser());
        btnAgregar.setEnabled(false);
        comboNivel.setSelectedIndex(objeto.getNivel() + 1);

        jButton3.setEnabled(true);
    }

