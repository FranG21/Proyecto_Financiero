    public boolean validarCampo() {
        boolean validado = true;
        if (txtContraseña.getText().equals("")) {
            validado = false;
        }
        if (comboNivel.getSelectedIndex() == 0) {
            validado = false;
        }
        return validado;
    }

    public void limpiar() {

        txtContraseña.setText("");
        comboNivel.setSelectedIndex(0);
        txtUsuario.setText("");
        txtNuvaContraseña.setText("");
        checkCambio.setVisible(false);
    }

    public void acctualizarTabla() {
        usuarios = new ArrayList<>();
        usuarios = ctrUsuario.listaUsuarios();

        modelo.setRowCount(usuarios.size());
        for (int i = 0; i < usuarios.size(); i++) {
            modelo.setValueAt(i + 1, i, 0);
            modelo.setValueAt(usuarios.get(i).getUser(), i, 1);
            modelo.setValueAt(usuarios.get(i).getPass(), i, 2);
            if (usuarios.get(i).getNivel() == 0) {
                modelo.setValueAt("Administrador", i, 3);
            } else if (usuarios.get(i).getNivel() == 1) {
                modelo.setValueAt("Gerente de activo fijo", i, 3);
            } else {
                modelo.setValueAt("Gerente de cuentas por cobrar", i, 3);
            }
        }
        tblDatos.setModel(modelo);
    }
