    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:

        if (validarCampo()) {
            if (ctrUsuario.buscarUsuario(txtUsuario.getText()) == null) {
                String usuario = txtUsuario.getText();
                String contraseña = Encriptacion.getStringMessageDigest(txtContraseña.getText(), Encriptacion.SHA256);
                int tipoUsu = comboNivel.getSelectedIndex() - 1;
                Usuario obj = new Usuario(usuario, contraseña, tipoUsu);
                ctrUsuario.AgregarUsuario(obj);

                acctualizarTabla();

                limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "Nombre de usuario ya registrado");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Campos necesarios");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void jbtmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtmodificarActionPerformed
        // TODO add your handling code here:

        if (checkCambio.isSelected()) {
            if (validarCampo()) {
                if (objeto.getPass().equals(Encriptacion.getStringMessageDigest(txtContraseña.getText(), Encriptacion.SHA256))) {
                    objeto.setPass(Encriptacion.getStringMessageDigest(txtNuvaContraseña.getText(), Encriptacion.SHA256));
                    objeto.setNivel(comboNivel.getSelectedIndex() - 1);

                    lblNueva.setVisible(false);
                    txtNuvaContraseña.setVisible(false);
                    jbtmodificar.setEnabled(false);
                    jButton3.setEnabled(false);
                    btnAgregar.setEnabled(true);
                    txtUsuario.setEditable(true);
                    ctrUsuario.ModificarUsuario(objeto);
                    limpiar();
                    acctualizarTabla();
                    JOptionPane.showMessageDialog(null, "Datos Modificados");
                } else {
                    JOptionPane.showMessageDialog(null, "Contraseña incorrecta, ingrese la contraseña antigua\n para poder cambiar contraseña");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Campos necesarios");
            }
        } else {

            objeto.setNivel(comboNivel.getSelectedIndex() - 1);

            lblNueva.setVisible(false);
            txtNuvaContraseña.setVisible(false);
            jbtmodificar.setEnabled(false);
            jButton3.setEnabled(false);
            btnAgregar.setEnabled(true);
            txtUsuario.setEditable(true);
            ctrUsuario.ModificarUsuario(objeto);
            limpiar();
            acctualizarTabla();
            JOptionPane.showMessageDialog(null, "Datos Modificados");
        }


    }//GEN-LAST:event_jbtmodificarActionPerformed

